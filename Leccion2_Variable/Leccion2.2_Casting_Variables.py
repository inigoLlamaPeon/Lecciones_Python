# Casting Variables
# -----------------

# Original

a = 5
b = 1.3
c = "1.5"

print("a = ", a)
print("Type of a ->", type(a))
print("b = ", b)
print("Type of b ->", type(b))
print("c = ", c)
print("Type of c ->", type(c))
print("-------------------------------------------")
